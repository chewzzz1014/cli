# wc: word count, show total count of words in a file 
# display num of lines, num of words and num of bytes
wc peanuts

# -l: get num of lines only
# -w: get num of words only
# -c: get num of bytes only
wc -c peanuts

# nl: number lines, check the count of lines in a file 
# eg. 
# 1. ... 
# 2. ...
nl peanuts